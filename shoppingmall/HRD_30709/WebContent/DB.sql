-- 회원목록조회/수정
SELECT 
	  CUSTNO
	, CUSTNAME
	, PHONE
	, ADDRESS
	, TO_CHAR(JOINDATE, 'YYYYMMDD') JOINDATE
	, GRADE
	, CITY
FROM MEMBER_TBL;

--회원매출조회
SELECT
	  A.CUSTNO
	, A.CUSTNAME  
	, A.GRADE
	, SUM(B.PRICE)
FROM 
	MEMBER_TBL A, MONEY_TBL B
WHERE
	A.CUSTNO = B.CUSTNO
GROUP BY 
	A.CUSTNO, A.CUSTNAME, A.GRADE
ORDER BY 4 DESC;